package Person;


public class Person {
	private String name;
	private String superpower;
	public String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	public String getSuperpower() {
		return superpower;
	}
	void setSuperpower(String superpower) {
		this.superpower = superpower;
	}
	
}
